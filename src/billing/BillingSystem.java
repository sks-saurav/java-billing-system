    private void bCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCalculateActionPerformed
        double f = Double.parseDouble(jTextField1.getText()) * fruit;
        double v = Double.parseDouble(jTextField2.getText()) * veges;
        double p = Double.parseDouble(jTextField3.getText()) * pizza;
        double w = Double.parseDouble(jTextField4.getText()) * wafer;
        double b = Double.parseDouble(jTextField5.getText()) * biscuit;
        
        double totalval = f + v + p + w + b;
        double del = totalval * 0.05;
        double mil = del * 0.5;
        
        double subtotal = totalval + del + mil;
        double cgst = subtotal * 0.09;
        double sgst = cgst;
        
        double tax = cgst + sgst;
        double amount = subtotal + tax;
        
        jTextField6.setText(String.format("%.2f", totalval));
        jTextField7.setText(String.format("%.2f", del));
        jTextField8.setText(String.format("%.2f", mil));
        jTextField9.setText(String.format("%.2f", cgst));
        jTextField10.setText(String.format("%.2f", sgst));
        jTextField12.setText(String.format("%.2f", subtotal));
        jTextField13.setText(String.format("%.2f", tax));
        jTextField11.setText(String.format("%.2f", amount));
    }//GEN-LAST:event_bCalculateActionPerformed


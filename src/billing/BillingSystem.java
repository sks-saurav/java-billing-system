    private void bansActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bansActionPerformed
        snum = Double.parseDouble(txtDisplay.getText());
        
        switch(operation){
            case "+": res = fnum + snum;
                       answer = String.format("%.2f", res);
                       txtDisplay.setText(answer);
                       break;
            case "-": res = fnum - snum;
                       answer = String.format("%.2f", res);
                       txtDisplay.setText(answer);
                       break;
            case "*": res = fnum * snum;
                       answer = String.format("%.2f", res);
                       txtDisplay.setText(answer);
                       break;
            case "/": res = fnum + snum;
                       answer = String.format("%.2f", res);
                       txtDisplay.setText(answer);
                       break;
            default: res = fnum % snum;
                       answer = String.format("%.2f", res);
                       txtDisplay.setText(answer);    
        }
    }//GEN-LAST:event_bansActionPerformed

